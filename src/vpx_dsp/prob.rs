unsafe extern "C" fn tree_merge_probs_impl(
    mut i: ::core::ffi::c_uint,
    mut tree: *const vpx_tree_index,
    mut pre_probs: *const vpx_prob,
    mut counts: *const ::core::ffi::c_uint,
    mut probs: *mut vpx_prob,
) -> ::core::ffi::c_uint {
    unsafe {
        let l: ::core::ffi::c_int = *tree.offset(i as isize) as ::core::ffi::c_int;
        let left_count: ::core::ffi::c_uint = if l <= 0 as ::core::ffi::c_int {
            *counts.offset(-l as isize)
        } else {
            tree_merge_probs_impl(l as ::core::ffi::c_uint, tree, pre_probs, counts, probs)
                as ::core::ffi::c_uint
        };
        let r: ::core::ffi::c_int =
            *tree.offset(i.wrapping_add(1 as ::core::ffi::c_uint) as isize) as ::core::ffi::c_int;
        let right_count: ::core::ffi::c_uint = if r <= 0 as ::core::ffi::c_int {
            *counts.offset(-r as isize)
        } else {
            tree_merge_probs_impl(r as ::core::ffi::c_uint, tree, pre_probs, counts, probs)
                as ::core::ffi::c_uint
        };
        let ct: [::core::ffi::c_uint; 2] = [left_count, right_count];
        *probs.offset((i >> 1 as ::core::ffi::c_int) as isize) = mode_mv_merge_probs(
            *pre_probs.offset((i >> 1 as ::core::ffi::c_int) as isize),
            &raw const ct as *const ::core::ffi::c_uint,
        );
        return left_count.wrapping_add(right_count);
    }
}
